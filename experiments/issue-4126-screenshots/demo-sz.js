// План отличается от факта, чтобы колонка «%» выглядела осмысленно.
const RATIOS = [1.03, 0.98, 1.05, 1.0, 1.07, 0.96, 1.02, 0.99];

const POINT_DATE = '20260101';

function point(val) {
  return JSON.stringify({ date: POINT_DATE, val: String(val) });
}

function planFor(fact, idx, isPct) {
  const ratio = RATIOS[idx % RATIOS.length];
  if (isPct) return Math.max(1, Math.round(fact / ratio));
  const raw = fact / ratio;
  return fact >= 1000000 ? Math.round(raw / 1000) * 1000 : Math.round(raw);
}

/** Фид значений по колонкам (report/Дэшборд.ЗначенияЗаПериод). */
function ceoValuesFeed() {
  const out = [];
  const push = (item, column, val) => out.push({
    item,
    value: point(val),
    'Столбец бюджета': '',
    'Колонка группы': column,
    valueItemID: ITEM_ID[item] || '',
    RGcolumnsID: column,
    'Метка': '',
  });

  Object.entries(CEO).forEach(([item, raw], idx) => {
    const isPct = typeof raw === 'string';
    const fact = isPct ? Number(String(raw).replace('%', '')) : raw;
    push(item, 'План', planFor(fact, idx, isPct));
    push(item, 'Факт', fact);
  });
  return out;
}

function fmt(v) {
  if (typeof v === 'string') return v;
  if (v === 0) return '';
  return new Intl.NumberFormat('ru-RU').format(v);
}


/**
 * Заполняет пустые ячейки дашборда демо-значениями и убирает служебный лист
 * «Секретный HR». Структура (листы, панели, показатели) остаётся оригинальной.
 */
function fillDashboard(realJson) {
  let rows;
  try { rows = JSON.parse(realJson); } catch (_) { return []; }
  if (!Array.isArray(rows)) return rows;

  rows = rows.filter((r) => r.sheet !== 'Секретный HR');

  // Многоколоночные панели берут значения из фида ceoValuesFeed (План/Факт).
  // Однокол`оночные («HR», «План продаж») читают предустановленное значение
  // из самой модели — оно привязано к itemID.
  const SINGLE_COLUMN = new Set(['HR', 'План продаж']);
  for (const r of rows) {
    if (r.sheet !== 'Показатели для CEO' || !SINGLE_COLUMN.has(r.panel)) continue;
    if (Object.prototype.hasOwnProperty.call(CEO, r.item)) {
      const v = CEO[r.item];
      r.value = typeof v === 'string' ? v.replace('%', '') : fmt(v);
    }
  }

  // Панели вида Кол-во/Сумма/Конверсия оставляем пустыми: предустановленное
  // значение привязано к itemID, а не к колонке, и растеклось бы по всем трём.

  return rows;
}

