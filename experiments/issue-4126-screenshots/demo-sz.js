/** Парсит LIMIT=offset,count из URL. */
function parseLimit(url) {
  const m = /LIMIT=(\d+),(\d+)/i.exec(url);
  return m ? { offset: Number(m[1]), limit: Number(m[2]) } : { offset: 0, limit: 21 };
}

