// #4304 (продолжение) — красная плашка «задание разорвано по дням» на карточке очереди.
//
// Первая версия (#4304) показывала плашку ТОЛЬКО зафиксированным заданиям с «→», поэтому обычный
// просроченный разрыв авто-планирования оставался без предупреждения. Реальные данные ateh
// (Станок 3, 22.07): 638509+638515 = 158 проходов × 3.2 ≈ 506 мин при 470 мин нетто в смене
// (8:00–16:30 минус обед 40) — объём физически не влезает в день, хвост встаёт на 23.07 при сроке
// 22.07. Голова при этом В СРОК, просрочен только хвост.
//
// Правило теперь — два независимых случая:
//   А) просрочено + разорвано (авто-планирование);
//   Б) зафиксировано + разорвано (ручной перенос, не влезло в день).
// Разрыв не обязан быть на 2 дня: объём может не влезть и в 2, и в 3 смены — метится ЛЮБОЙ сегмент
// цепочки (есть «←» и/или «→»), а не только голова.
//
// Run with: node experiments/atex-production-planning-4304-split-warn.test.js

process.env.TZ = 'UTC';
var planning = require('../download/atex/js/production-planning.js').planning;
var warn = planning.daySplitWarning;

var passed = 0;
function assert(cond, name) {
    console.log((cond ? 'PASS' : 'FAIL') + ' — ' + name);
    if (cond) passed++; else process.exitCode = 1;
}

// ── А) просрочено + разорвано: авто-планирование, предупреждение есть на всех «рваных» сегментах ──
(function () {
    var head = warn({ toNext: true, overdue: true, fixed: false });                  // голова (день 1)
    var mid = warn({ fromPrev: true, toNext: true, overdue: true, fixed: false });   // середина (день 2 из 3)
    var tail = warn({ fromPrev: true, overdue: true, fixed: false });                // хвост (последний день)
    assert(!!head && !!mid && !!tail, 'А: просроченный разрыв — плашка на голове, середине И хвосте (разрыв бывает на 3+ дня)');
    assert(!!tail && /просрочено/.test(tail.text) && /до срока/.test(tail.text),
        'А: текст называет просрочку — «⚠ Задание разорвано по дням и просрочено — не помещается в смену до срока»');
    assert(!!tail && !/Зафиксированное/.test(tail.text),
        'А: незафиксированное задание не называется зафиксированным');
})();

// ── Б) зафиксировано + разорвано: ручной перенос, не влезло в день (поведение #4304 сохранено) ────
(function () {
    var w = warn({ toNext: true, fixed: true, overdue: false });
    assert(!!w && /^⚠ Зафиксированное задание разорвано по дням — не помещается в смену$/.test(w.text),
        'Б: зафикс-разрыв в срок — «⚠ Зафиксированное задание разорвано по дням — не помещается в смену»');
    assert(!!warn({ fromPrev: true, fixed: true, overdue: false }),
        'Б: зафиксированный сегмент-продолжение («←») тоже предупреждает (не только голова с «→»)');
    var both = warn({ fromPrev: true, toNext: true, fixed: true, overdue: true });
    assert(!!both && /Зафиксированное/.test(both.text) && /просрочено/.test(both.text),
        'А+Б: зафиксировано И просрочено — в тексте оба признака');
})();

// ── Контроль: молчим там, где предупреждать не о чем ──────────────────────────────────────────────
(function () {
    assert(warn({ fromPrev: false, toNext: false, overdue: true, fixed: true }) === null,
        'контроль: просроченное/зафиксированное, но ЦЕЛОЕ задание (нет ←/→) — плашки нет');
    assert(warn({ toNext: true, overdue: false, fixed: false }) === null,
        'контроль: штатное дробление большого задания в срок и без фиксации — плашки нет');
    assert(warn({}) === null && warn(null) === null,
        'контроль: пустой ввод — null (без исключений)');
})();

console.log('\n' + passed + ' assertions passed');
