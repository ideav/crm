#!/usr/bin/env bash
# Creates one GitHub ticket per atex workplace (issue #2903).
# Each ticket references the parent issue #2903 and docs/atex_workplaces.md.
# Idempotency: skips creation if an OPEN issue with the same title already exists.
set -euo pipefail

REPO="ideav/crm"
PARENT=2903
PLAN="docs/atex_workplaces.md"
SPEC="https://github.com/ideav/atex/blob/main/docs/superpowers/specs/2026-05-26-atex-schema-setup-design.md"
OUT="${1:-/tmp/atex-workplace-tickets.md}"
: > "$OUT"

# title | role | section | tables | summary | api | acceptance
ROWS=$(cat <<'EOF'
atex: рабочее место «Приём и ведение заказов» (Менеджер)|Менеджер|3.1|Заказ, Позиция заказа ⊂ Заказ, Клиент, Вид сырья, Тип резки|Создание заказа с позициями, выбор клиента/вида сырья/типа резки, смена статусов заказа и позиций, список заказов с фильтром.|Запись: `_m_new/{Заказ}`, позиции `_m_new/{Позиция заказа}` с `up={orderId}`, правки `_m_set`. Чтение: `object/{Заказ}/?JSON_KV`, позиции `object/{Позиция заказа}/?F_U={orderId}`.|Менеджер создаёт заказ с ≥1 позицией; позиция привязана к заказу через `up`; статусы меняются; список отражает изменения.
atex: рабочее место «Калькулятор типов резки» (Диспетчер)|Диспетчер|3.2|Тип резки, Полоса ⊂ Тип резки, Вид сырья|Создание/правка типа резки, ввод полос (ширина × количество, назначение), расчёт «Итого ножей» и «Остаток, мм».|Запись: `_m_new/{Тип резки}`, полосы `_m_new/{Полоса}` с `up={cutTypeId}`, пересчёт на клиенте → `_m_set`.|Тип резки с набором полос; сумма полос и остаток считаются верно; полосы привязаны к типу резки.
atex: рабочее место «Планирование производства» (Диспетчер)|Диспетчер|3.3|Производственная резка, Обеспечение ⊂ Позиция заказа, Позиция заказа, Слиттер, Тип резки, Партия сырья|Создание производственной резки (слиттер, тип резки, партия сырья, дата плана), очередь резок по слиттерам, привязка резки к позициям заказа через «Обеспечение».|Запись: `_m_new/{Производственная резка}` (Номер `unique` сам считает), `Обеспечение` `_m_new` с `up={позицияId}` и `t{refРезка}`. Чтение очереди: `object/{Производственная резка}/?JSON_KV`.|Резка создаётся с автонумером; обеспечение связывает позицию заказа и резку (M:N через подчинённую таблицу).
atex: рабочее место «Приёмка сырья» (Кладовщик)|Кладовщик|3.4|Партия сырья, Вид сырья|Оприходование партии сырья (вид сырья, дата прихода, получено м²), ведение остатка FIFO.|Запись: `_m_new/{Партия сырья}`, остаток `_m_set`. Чтение: `object/{Партия сырья}/?JSON_KV` с сортировкой по дате прихода.|Партия создаётся со ссылкой на вид сырья; «Остаток, м²» инициализируется значением «Получено, м²».
atex: рабочее место «Пульт слиттера» (Оператор)|Оператор|3.5|Производственная резка, Расход сырья ⊂ резка, Событие смены, Партия сырья|Планшет оператора: смена статуса резки, показания счётчика (нач./кон.), погонаж факт, брак; списание расхода сырья (FIFO); фиксация событий смены.|Запись: `_m_set/{резкаId}`, расход `_m_new/{Расход сырья}` с `up={резкаId}`, событие `_m_new/{Событие смены}`. Чтение «мои резки»: `object/{...}` с фильтром по слиттеру/статусу.|Оператор переводит резку по статусам; расход сырья уменьшает остаток партии; события смены пишутся с датой/временем.
atex: рабочее место «Пульт втулкореза» (Оператор)|Оператор|3.6|Задание на втулки ⊂ Позиция заказа, Втулкорез|Планшет оператора: по заданию на втулки выбрать втулкорез, ввести кол-во факт, менять статус.|Чтение позиций: `report/orders_list?JSON_KV`; чтение заданий: `object/{Задание на втулки}/?F_U={позицияId}`; правки `_m_set`; новые задания `_m_new/{Задание на втулки}` с `up={позицияId}`.|Задание привязано к позиции заказа; статус и кол-во факт сохраняются; выбор втулкореза работает.
atex: рабочее место «Карта раскроя» (Оператор)|Оператор|3.7|Производственная резка, Тип резки, Полоса|Визуализация раскроя текущей резки: полосы типа резки на ширине входа, остаток. Преимущественно чтение.|Чтение: `object/{Производственная резка}/?F_I={резкаId}` → `object/{Полоса}/?F_U={типРезкиId}`.|Карта отрисовывает все полосы выбранной резки с корректными ширинами и остатком.
atex: рабочее место «Склад готовой продукции» (Кладовщик)|Кладовщик|3.8|Партия ГП, Обеспечение ⊂ Позиция заказа, Производственная резка|Оприходование партии ГП из завершённой резки (ширина, кол-во рулонов, метраж, адрес хранения), смена статуса (Есть → Зарезервирован → Отгружен), FIFO-списание в обеспечение позиций.|Запись: `_m_new/{Партия ГП}`, статус/резерв `_m_set`, списание `_m_set` у `Обеспечение` (`t{refПартияГП}`). Чтение: `object/{Партия ГП}/?JSON_KV`.|Партия ГП создаётся из резки; резерв/отгрузка меняют статус; обеспечение позиции ссылается на партию ГП.
atex: рабочее место «Дашборды и отчёты» (Руководитель)|Руководитель|3.9|(чтение) Заказ, Производственная резка, Партия ГП, Партия сырья|Сводки только для чтения: заказы по статусам, загрузка слиттеров, выпуск ГП, остатки сырья.|Чтение: `object/{typeId}/?JSON_KV&_count=` для счётчиков, постранично для списков. Кандидат №1 на перевод чтений на `report/` после появления защищённого слоя данных.|Дашборд показывает корректные счётчики/списки по живым данным.
atex: рабочее место «Клиентский портал» (Клиент) — следующая фаза|Клиент|3.10|(чтение) Заказ, Позиция заказа — только свои|Клиент видит только свои заказы и статусы (позже — самостоятельное создание заказа).|На первом этапе `object/{Заказ}/?JSON_KV` с фильтром по клиенту; изоляция данных опирается на права роли «Клиент» и слой `report/`.|Клиент видит исключительно свои заказы (после внедрения прав роли «Клиент»).
EOF
)

n=0
echo "$ROWS" | while IFS='|' read -r title role section tables summary api acceptance; do
  [ -z "$title" ] && continue
  n=$((n+1))
  export T="$title"
  # idempotency: skip if an open issue with this exact title exists
  existing=$(gh issue list --repo "$REPO" --state open --search "$title in:title" --json number,title \
    | python3 -c "import json,sys,os;t=os.environ['T'];print(next((str(i['number']) for i in json.load(sys.stdin) if i['title']==t),''))" 2>/dev/null) || existing=""
  if [ -n "$existing" ]; then
    url="https://github.com/$REPO/issues/$existing"
    echo "SKIP (exists): $url — $title" >&2
    echo "- [ ] [$title]($url)" >> "$OUT"
    continue
  fi
  body=$(cat <<BODY
Рабочее место atex (часть #$PARENT — «Подключи рабочие места по API»).

**Роль:** $role
**Ключевые таблицы:** $tables

## Что сделать
$summary

## API (на первом этапе — напрямую к таблицам, \`_m_*\`)
$api

> Указание из #$PARENT: «для начала обращайся напрямую к таблицам командами \`_m_*\`».
> Слой запросов (\`report/\`) для защиты данных — следующий этап; перевод чтений на
> \`report/\` делается отдельно.

## Критерии приёмки
$acceptance

## Контекст
- План декомпозиции: [\`$PLAN\`](https://github.com/$REPO/blob/main/$PLAN) — раздел $section
- Дизайн-спека atex: $SPEC
- Схема данных готова (#2901 / #2902); роли и пользователи — #2904.
- Шаблон рабочего места: \`templates/atex/\` + JS по правилам \`docs/WORKSPACE_DEVELOPMENT_GUIDE.md\`.

Родительская задача: #$PARENT
BODY
)
  url=$(gh issue create --repo "$REPO" --title "$title" --body "$body")
  echo "CREATED: $url" >&2
  echo "- [ ] [$title]($url)" >> "$OUT"
done

echo "=== Tickets written to $OUT ===" >&2
cat "$OUT" >&2
